import net.runelite.client.game.ItemManager;
import net.runelite.client.input.KeyManager;
import net.runelite.client.menus.MenuManager;
import net.runelite.client.plugins.Plugin;
import net.runelite.client.plugins.PluginDescriptor;

import javax.inject.Inject;
import java.util.*;

@PluginDescriptor(
	name = "Remove House Kick",
	description = "Remove kick option from houses",
	tags = {"house", "kick"},
	enabledByDefault = false
)
public class HouseRemoveKickPlugin extends Plugin
{
	@Inject
	private Client client;

	@Inject
	private HouseRemoveKickConfig config;


	@Subscribe
	public void onClientTick(ClientTick clientTick)
	{
		// The menu is not rebuilt when it is open, so don't swap or else it will
		// repeatedly swap entries
		if (client.getGameState() != GameState.LOGGED_IN || client.isMenuOpen())
		{
			return;
		}

		MenuEntry[] menuEntries = client.getMenuEntries();
		List<MenuEntry> entryList = new ArrayList<MenuEntry>();
		for (MenuEntry entry : menuEntries)
		{
			if(!entry.getOption().equals(config.getRemoveActions())){
				entryList.add(entry);
			}
		}
		MenuEntry[] newEntries = new MenuEntry[entryList.size()];
		newEntries = entryList.toArray(newEntries);

		//set and regrab for normal runelite functionality
		client.setMenuEntries(newEntries);
	}
}
